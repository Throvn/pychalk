def green(text):
    return '\033[92m' + text + '\033[0m'
    
def blue(text):
    return '\033[94m' + text + '\033[0m'

def yellow(text):
    return '\033[93m' + text + '\033[0m'

def red(text):
    return '\033[91m' + text + '\033[0m'

def bold(text):
    return '\033[1m' + text + '\033[0m'

def underline(text):
    return '\033[4m' + text + '\033[0m'
